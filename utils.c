#include "so_long.h"

int	valid_char(char c)
{
	if (c != '1' && c != '0' && c != 'C' && c != 'E' && c != 'P')
	{
		ft_printf("Error\nInvalid character in map: %c\n", c);
		return (1);
	}
	return (0);
}
